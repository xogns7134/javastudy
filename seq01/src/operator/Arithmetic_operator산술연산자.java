package operator;

public class Arithmetic_operator산술연산자 {
	public static void main(String[] args) {
		// 산술연산자(+ - * / %)
		
		int x = 220;
		int y = 100;
		
		System.out.println("두 수의 합은 "+(x + y));
		System.out.println("두 수의 차는 "+(x - y));
		System.out.println("두 수의 곱은 "+(x * y));
		System.out.println("두 수의 를 나눈 값의 몫은 "+(x / y));
		System.out.println("두 수의 를 나눈 값의 나머지는 "+(x % y));
		
	}
}
