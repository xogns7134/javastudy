package 부품만들기;

public class Dog {
	// 클래스 = 멤버변수(특징) + 멤버메서드(멤버함수)
	// 색, 이름, 사이즈, 품종
	public void 색() {
		System.out.println("금색");
	}
	public void 이름() {
		System.out.println("로빈");
	}
	public void 사이즈() {
		System.out.println("대형견");
	}
	public void 품종() {
		System.out.println("골든 리트리버");
	}
	public void 하는일() {
		System.out.println("어지럽히기");
	}
}
